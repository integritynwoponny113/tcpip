CREATE TABLE T_COUNTRY (
COUNTRY_NAME VARCHAR2(20) PRIMARY KEY,
GAME_COUNT NUMBER(10), 
PLAYER_COUNT NUMBER(10) 
)

CREATE TABLE T_GAME (
GAME_NAME VARCHAR2(60) PRIMARY KEY,
STADIUM_LOCATION VARCHAR2(60), --DETAILGAME_NAME로 수정
FULL_SCHEDULE VARCHAR2(20)
)

CREATE TABLE T_COUNTRYGAME(
COUNTRY_NAME VARCHAR2(20),
GAME_NAME VARCHAR2(60) 
)

ALTER TABLE T_COUNTRYGAME ADD CONSTRAINT PKOL PRIMARY KEY 
(COUNTRY_NAME, GAME_NAME);


ALTER TABLE T_COUNTRYGAME ADD CONSTRAINT FKOL1
FOREIGN KEY (COUNTRY_NAME) REFERENCES T_COUNTRY(COUNTRY_NAME)

ALTER TABLE T_COUNTRYGAME ADD CONSTRAINT FKOL2
FOREIGN KEY (GAME_NAME) REFERENCES T_GAME(GAME_NAME)


